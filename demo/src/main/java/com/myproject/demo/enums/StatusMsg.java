package com.myproject.demo.enums;

public enum StatusMsg {
    /**
     *未完成
     */
    UNDONE("0","未完成"),

    /**
     * 已完成
     */
    COMPLETED("1","已完成");

    private String code;

    private String msg;

    StatusMsg(String code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
