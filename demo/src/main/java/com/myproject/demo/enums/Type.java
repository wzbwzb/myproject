package com.myproject.demo.enums;

public enum Type {

    WORK(1,"工作"),

    LIFE(2,"生活"),

    OTHER(3,"其他");

    private int code;

    private String msg;

    Type(int code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
